package baena.skyline;

public class Building {
	
	private int x1;
	private int x2;
	private int h;
	
	public Building(int x1, int x2, int h) {
		this.x1 = x1;
		this.x2 = x2;
		this.h = h;
	}

	public int getX1() {
		return x1;
	}

	public int getX2() {
		return x2;
	}

	public int getH() {
		return h;
	}
}
